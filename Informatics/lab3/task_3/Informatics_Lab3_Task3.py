from re import *


def except_cron(text):
    pattern = (r"0*[1-5]?[*0-9]"
               r"((((?<=\d)-0*[1-5]?[0-9])"
               r"|(,0*[1-5]?[*0-9])+"
               r"|(/0*[1-5]?[0-9])))?"
               r"\s0*(1?[*0-9]|2[*0-3])"
               r"((((?<=\d)-0*(1?[*0-9]|2[0-3]))"
               r"|(,0*(1?[*0-9]|2[*0-3])+)"
               r"|(/0*(1?[*0-9]|2[0-3]))))?"
               r"\s0*([1-9]?|[1-2]?[*0-9]|3[*0-1])"
               r"((((?<=\d)-0*([1-9]?|[1-2]?[*0-9]|3[*0-1]))"
               r"|(,0*([1-9]?|[1-2]?[*0-9]|3[*0-1])+)"
               r"|(/0*([1-9]?|[1-2]?[*0-9]|3[*0-1]))))?"
               r"\s0*([1-9]?|1?[*0-2])"
               r"((((?<=\d)-0*([1-9]?|1?[*0-2]))"
               r"|(,0*([1-9]?|1?[*0-2])+)"
               r"|(/0*([1-9]?|1?[*0-2]))))?"
               r"\s0*[*0-6]"
               r"((((?<=\d)-0*[*0-6])"
               r"|(,0*[*0-6])+"
               r"|(/0*[*0-6])))?")
    match = fullmatch(pattern, text)
    if match:
        return match.group()
    return match
